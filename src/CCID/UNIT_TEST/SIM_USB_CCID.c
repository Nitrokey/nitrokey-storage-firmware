/******************************************************************************

	Simulation_USB_CCID

******************************************************************************/

int Simulation_USB_CCID (void)
{
    t_USB_CCID_data_st USB_CCID_data_st;

    nSimEvent = 0;
    while (NULL != USB_Log[nSimEvent])
    {
        if ('O' == USB_Log[nSimEvent][0])
        {
            memcpy (USB_CCID_data_st.USB_data, Build_USB_Data (USB_Log[nSimEvent]), USB_CCID_MAX_LENGTH);
            USB_CCID_data_st.CCID_datalen = USB_CCID_data_st.USB_data[CCID_OFFSET_LENGTH + 1] * 256 + USB_CCID_data_st.USB_data[CCID_OFFSET_LENGTH];

#ifndef CCID_NO_PRINTF
            Print_USB_CCID_Command (USB_Log[nSimEvent]);
#endif
            USB_to_CRD_DispatchUSBMessage_v (&USB_CCID_data_st);

#ifndef CCID_NO_PRINTF
            Print_USB_CCID_ERROR (&USB_CCID_data_st, USB_Log[nSimEvent + 1]);
#endif

            if (FALSE == Check_USB_CCID_Answer (&USB_CCID_data_st, USB_Log[nSimEvent + 1]))
            {
                return (FALSE);
#ifndef CCID_NO_PRINTF
                printf ("\nERROR\n");
#endif
            }
#ifndef CCID_NO_PRINTF
            printf ("\n\n");
#endif
            nSimEvent++;
        }
        nSimEvent++;
    }

    return (TRUE);
}
