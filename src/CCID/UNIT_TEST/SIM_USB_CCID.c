/******************************************************************************

	Build_USB_Data

******************************************************************************/

char* Build_USB_Data (char* SimData)
{
    char* p;
    int value;
    int i;
    static char USB_Data[CCID_MAX_XFER_LENGTH];

    p = &SimData[3];

    i = 0;
    while (NULL != p)
    {
        value = strtoul (p, &p, 16);
        USB_Data[i] = (unsigned char) value;
        i++;
        if (0 == p[1])
        {
            break;
        }
    }
    return (USB_Data);
}

